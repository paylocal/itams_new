const fs = require("fs");
const path = require("path");

const file = path.join(__dirname, "src/components/i18n-provider.tsx");

const code = [
  '"use client";',
  'import { createContext, useContext, useState, useEffect, useCallback, ReactNode } from "react";',
  'import viJSON from "../messages/vi/common.json";',
  'import enJSON from "../messages/en/common.json";',
  "",
  'type Locale = string;',
  "",
  'interface I18nContextType {',
  '  locale: Locale;',
  '  setLocale: (l: Locale) => void;',
  '  t: (key: string) => string;',
  '  languages: { code: string; name: string; flag: string | null }[];',
  '  loading: boolean;',
  "}",
  "",
  'const fallbackData = { vi: viJSON, en: enJSON };',
  "",
  'function safeGet(key) {',
  '  try {',
  '    if (typeof window === "undefined") return null;',
  '    return localStorage.getItem(key);',
  '  } catch { return null; }',
  "}",
  "",
  'function safeSet(key, value) {',
  '  try {',
  '    if (typeof window === "undefined") return;',
  '    localStorage.setItem(key, value);',
  "  } catch {}",
  "}",
  "",
  'const I18nContext = createContext<I18nContextType>({',
  '  locale: "vi",',
  '  setLocale: () => {},',
  '  t: (k) => k,',
  '  languages: [],',
  '  loading: true,',
  "});",
  "",
  'export function I18nProvider({ children }: { children: ReactNode }) {',
  '  const [locale, setLocaleState] = useState<Locale>("vi");',
  '  const [languages, setLanguages] = useState<{code:string;name:string;flag:string|null}[]>([]);',
  '  const [dbTranslations, setDbTranslations] = useState<Record<string, string>>({});',
  '  const [loading, setLoading] = useState(true);',
  "",
  '  useEffect(() => {',
  '    (async () => {',
  '      try {',
  '        const res = await fetch("/api/languages", { cache: "no-store" });',
  '        if (res.ok) {',
  '          const langs = await res.json();',
  "          console.log(\"Loaded languages:\", langs.length);",
  "          setLanguages(langs);",
  '          const saved = safeGet("locale");',
  '          const valid = langs.find((l) => l.code === saved);',
  "          if (valid) {",
  '            setLocaleState(saved as string);',
  "          } else {",
  '            const def = langs.find((l) => l.isDefault) || langs[0];',
  "            if (def) setLocaleState(def.code);",
  "          }",
  "        }",
  "      } catch (e) { console.error(e); }",
  "    })();",
  "  }, []);",
  "",
  '  useEffect(() => {',
  "    if (!locale) return;",
  "    (async () => {",
  "      try {",
  '        setLoading(true);',
  '        const res = await fetch("/api/translations/" + locale, { cache: "no-store" });',
  "        if (res.ok) {",
  "          const data = await res.json();",
  '          setDbTranslations(data || {});',
  "        }",
  "      } catch (e) { console.error(e); }",
  '      finally { setLoading(false); }',
  "    })();",
  "  }, [locale]);",
  "",
  '  const setLocale = (l: Locale) => {',
  "    setLocaleState(l);",
  '    safeSet("locale", l);',
  "  };",
  "",
  '  const t = useCallback(',
  "    (key: string): string => {",
  '      if (dbTranslations[key]) return dbTranslations[key];',
  "      const getNested = (obj: any, k: string) =>",
  '        k.split(".").reduce((o, i) => (o ? o[i] : null), obj);',
  "      const fromFile = getNested(",
  "        fallbackData[locale] || fallbackData.vi,",
  "        key",
  "      );",
  "      if (fromFile) return fromFile;",
  "      return key;",
  "    },",
  "    [dbTranslations, locale]",
  "  );",
  "",
  "  return (",
  "    <I18nContext.Provider",
  "      value={{ locale, setLocale, t, languages, loading }}",
  "    >",
  "      {children}",
  "    </I18nContext.Provider>",
  "  );",
  "}",
  "",
  'export function useI18n() {',
  "  return useContext(I18nContext);",
  "}",
].join("\n");

fs.writeFileSync(file, code, "utf-8");
console.log("Created:", file);
console.log("Size:", fs.statSync(file).size, "bytes");