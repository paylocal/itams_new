const fs = require("fs");
const path = require("path");

// Xoa thu muc migrations
const migrationsDir = path.join(__dirname, "prisma/migrations");
if (fs.existsSync(migrationsDir)) {
  fs.rmSync(migrationsDir, { recursive: true, force: true });
  console.log("Deleted migrations directory");
}

// Giu lai migration_lock.toml
const lockFile = path.join(migrationsDir, "migration_lock.toml");
const lockContent = `# Please do not edit this file manually
# It should be added in your version-control system (e.g., Git)
provider = "mssql"
`;

fs.mkdirSync(migrationsDir, { recursive: true });
fs.writeFileSync(lockFile, lockContent);
console.log("Created migration_lock.toml for mssql");

// Xoa dev.db
const dbFile = path.join(__dirname, "prisma/dev.db");
if (fs.existsSync(dbFile)) {
  fs.unlinkSync(dbFile);
  console.log("Deleted dev.db");
}

console.log("Done");